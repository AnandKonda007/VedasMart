package com.example.vedasmart.ServerResponseModels;

public class SubCategorysModel {
    private String subCategoryID;
    private String subCategoryName;
    private String SubCategoryProfilePic;

    public String getSubCategoryID() {
        return subCategoryID;
    }

    public void setSubCategoryID(String subCategoryID) {
        this.subCategoryID = subCategoryID;
    }

    public String getSubCategoryName() {
        return subCategoryName;
    }

    public void setSubCategoryName(String subCategoryName) {
        this.subCategoryName = subCategoryName;
    }

    public String getSubCategoryProfilePic() {
        return SubCategoryProfilePic;
    }

    public void setSubCategoryProfilePic(String subCategoryProfilePic) {
        SubCategoryProfilePic = subCategoryProfilePic;
    }
}
